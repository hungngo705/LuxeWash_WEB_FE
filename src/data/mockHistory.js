export function getHistoryStats(records) {
  const completed = records.filter((r) => r.bookingStatus === 'Completed').length
  const cancelled = records.filter((r) => r.bookingStatus === 'Cancelled').length
  const revenue = records
    .filter((r) => r.transactionStatus === 'Success')
    .reduce((sum, r) => sum + r.totalAmount, 0)
  return { total: records.length, completed, cancelled, revenue }
}
